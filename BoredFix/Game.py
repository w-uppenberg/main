# Game.py

class Game:
  def __init__(title, creator, retail_price):
    self.title = title
    self.creator = creator
    self.retail_price = retail_price
    self.usages = 0

def __str__(self):
  return f"Game: {self.title}, Creator: {self.creator}, Retail Price: {self.retail_price}, Usages: {self.usages}"

